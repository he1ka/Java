package com.company.WebShop;

public class Customer {
    String lastName;
    String firstName;
    String company;
    String email;
    String address;
    String phone;

    public Customer(String lastName, String firstName, String company, String email, String address, String phone) {
        this.lastName = lastName;
        this.firstName = firstName;
        this.company = company;
        this.email = email;
        this.address = address;
        this.phone = phone;
    }
}
