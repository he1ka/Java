package com.company.WebShop;

public class Employee {
    String lastName;
    String firstName;
    String email;
    String password;
    String jobTitle;

    public Employee(String lastName, String firstName, String email, String password, String jobTitle) {
        this.lastName = lastName;
        this.firstName = firstName;
        this.email = email;
        this.password = password;
        this.jobTitle = jobTitle;
    }
}
